static GBitmap bitmap = {
    .addr = &data,
    .row_size_bytes = 20,
    .info_flags = 0x1000,
    .bounds = {
        .origin = { .x = 0, .y = 0 },
        .size = { .w = 144, .h = 168 },
    },
};

Window *window;
TextLayer *text_layer;
Layer *bitmap_layer;
GBitmap *bkgnd;

static struct TFlake{
    bool active;
    GPoint pos;
} flakes[MAX_FLAKES];

void reset_scene() {
    bmpFill(&bitmap, GColorBlack);
    bmpCopy(bkgnd, &bitmap);
    int c;
    for (c = 0; c < MAX_FLAKES; c++){
        flakes[c].active = 0;
        flakes[c].pos = GPoint(0, -1);
    }
}

static void update_display(Layer *layer, GContext *ctx) {
	graphics_draw_bitmap_in_rect(ctx, &bitmap, GRect(0, 0, 144, 168));
    
    GRect bounds = layer_get_bounds(layer);
    GRect frame = GRect(0, bounds.size.h - 34, bounds.size.w, bounds.size.h);

    time_t def_time;
    struct tm *now;
    def_time = time(NULL);
	now = localtime(&def_time);    

    static char txt[] = "00:00"; // Needs to be static because it's used by the system later.
    strftime(txt, sizeof(txt), "%H:%M", now);

    graphics_context_set_text_color(ctx, GColorWhite);
    graphics_draw_text(ctx, 
                       txt,
                       fonts_get_system_font(FONT_KEY_BITHAM_30_BLACK),
                       frame,
                       GTextOverflowModeTrailingEllipsis,
                       GTextAlignmentCenter,
                       NULL
                      );
}

int isEmpty(int x, int y){
    if ((bmpGetPixel(&bitmap, x * 2, y * 2) == GColorBlack) &&
        (bmpGetPixel(&bitmap, x * 2 + 1, y * 2) == GColorBlack) &&
        (bmpGetPixel(&bitmap, x * 2, y * 2 + 1) == GColorBlack) &&
        (bmpGetPixel(&bitmap, x * 2 + 1, y * 2 + 1) == GColorBlack))
        return 1;
    else
        return 0;
}

void handle_tick(struct tm *tick_time, TimeUnits units_changed) {
    int c;
    for (c = 0; c < MAX_FLAKES; c++)
        if (flakes[c].active){
            if (flakes[c].pos.y != -1) {
                if (flakes[c].pos.y == 83)
                    flakes[c].pos.y = -1;
                else if (!isEmpty(flakes[c].pos.x, flakes[c].pos.y + 1)){
                    // blocked below
                    if ((flakes[c].pos.x > 0) && isEmpty(flakes[c].pos.x - 1, flakes[c].pos.y + 1)){
                        bmpFillRect(&bitmap, GRect(flakes[c].pos.x * 2, flakes[c].pos.y * 2, 2, 2), GColorBlack);
                        flakes[c].pos.y++;
                        if ((flakes[c].pos.x < 71) && isEmpty(flakes[c].pos.x + 1, flakes[c].pos.y + 1) && (rand()%2 == 0))
                            flakes[c].pos.x++;
                        else
                            flakes[c].pos.x--;
                    }else if ((flakes[c].pos.x < 71) && isEmpty(flakes[c].pos.x + 1, flakes[c].pos.y + 1)){
                        bmpFillRect(&bitmap, GRect(flakes[c].pos.x * 2, flakes[c].pos.y * 2, 2, 2), GColorBlack);
                        flakes[c].pos.y++;
                        flakes[c].pos.x++;
                    }else
                        flakes[c].pos.y = -1;
                } else {
                    bmpFillRect(&bitmap, GRect(flakes[c].pos.x * 2, flakes[c].pos.y * 2, 2, 2), GColorBlack);
                    flakes[c].pos.y++;
                }
            }
            if (flakes[c].pos.y == -1)
                flakes[c].active = 0;
            else
                bmpFillRect(&bitmap, GRect(flakes[c].pos.x * 2, flakes[c].pos.y * 2, 2, 2), GColorWhite);
        }

    if (rand() % 4 == 0)
        for (c = 0; c < MAX_FLAKES; c++)
            if (!flakes[c].active){
                flakes[c].pos.x = rand() % 72;
                flakes[c].pos.y = 0;
                if (bmpGetPixel(&bitmap, flakes[c].pos.x * 2, 0) != GColorBlack){
                    // full
                    reset_scene();
                } else
                    flakes[c].active = 1;
                break;
        }
            
	layer_mark_dirty(bitmap_layer);
}

void handle_init(void) {
    // background from resouce
    bkgnd = gbitmap_create_with_resource(RESOURCE_ID_IMAGE_BKGND);
    
    // initialization
    reset_scene();

	// Create a window
	window = window_create();
    
    // Create text layer
	text_layer = text_layer_create(GRect(0, 0, 144, 154));
	
	// Set the text, font, and text alignment
	text_layer_set_text(text_layer, "Hi 2");
	text_layer_set_font(text_layer, fonts_get_system_font(FONT_KEY_GOTHIC_28_BOLD));
	text_layer_set_text_alignment(text_layer, GTextAlignmentLeft);
	
	// Add the text layer to the window
	layer_add_child(window_get_root_layer(window), text_layer_get_layer(text_layer));

    // Create bitmap layer
    bitmap_layer = layer_create(GRect(0, 0, 144, 168));
    layer_set_update_proc(bitmap_layer, update_display);
    
    // Add bitmap layer
    layer_add_child(window_get_root_layer(window), bitmap_layer);
   
	// Push the window
	window_stack_push(window, true);

    // tick event
    tick_timer_service_subscribe(SECOND_UNIT, &handle_tick);
}

void handle_deinit(void) {
	// Destroy the text layer
	text_layer_destroy(text_layer);

    layer_destroy(bitmap_layer);

    // background
    gbitmap_destroy(bkgnd);
    
	// Destroy the window
	window_destroy(window);
}

int main(void) {
	handle_init();
	app_event_loop();
	handle_deinit();
}
